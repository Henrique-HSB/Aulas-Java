package exercicio3;

                /*
     Imprima os números que vão de 1 até 10 utilizando o laço de repetição Enquanto (while)
                 */
public class ExecWhile {
    public static void main(String[] args) {
        int contador = 1;
        while(contador <= 10) {
            System.out.println(contador++);
        }
    }
}
